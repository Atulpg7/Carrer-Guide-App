public class Answer
{
    private int sNO;
    private String key;

    public Answer(int sNO, String key) {
        this.sNO = sNO;
        this.key = key;
    }

    public int getsNO() {
        return sNO;
    }

    public String getKey() {
        return key;
    }
}
